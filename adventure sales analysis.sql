--1. Retrieve the customers who have placed orders with a total amount greater than $10000. --

SELECT c.customerkey,
        CONCAT_WS(' ', c.Prefix, c.FirstName, c.LastName) AS CustomerName,
    ROUND(SUM(s.OrderQuantity * p.ProductPrice),2) AS TotalAmount
FROM sales s
JOIN productlookup p 
    ON s.ProductKey = p.ProductKey
JOIN customer c
    ON s.CustomerKey = c.CustomerKey
GROUP BY  c.customerkey,c.Prefix, c.FirstName, c.LastName
HAVING ROUND(SUM(s.OrderQuantity * p.ProductPrice),2) > 10000
ORDER BY TotalAmount DESC;


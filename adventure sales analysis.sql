--12.Find the customer names who have placed orders with a total amount greater than the average total amount of orders. – SQ --
 CREATE VIEW quest_12 AS
SELECT CONCAT(c.FirstName, ' ', c.LastName) AS CustomerName
FROM customer c
JOIN sales s ON c.CustomerKey = s.CustomerKey
JOIN productlookup p ON s.ProductKey = p.ProductKey
GROUP BY c.CustomerKey, c.FirstName, c.LastName
HAVING SUM(s.OrderQuantity * p.ProductPrice) > (
    SELECT AVG(total_amount) 
    FROM (
        SELECT SUM(s2.OrderQuantity * p2.ProductPrice) AS total_amount
        FROM sales s2
        JOIN productlookup p2 ON s2.ProductKey = p2.ProductKey
        GROUP BY s2.CustomerKey
    ) AS customer_totals
)
ORDER BY CustomerName;

SELECT * FROM quest_12


--2. Retrieve the total revenue and total orders generated by each product category --


WITH category_sales AS ( SELECT
      pc.CategoryName,
        ROUND((s.OrderQuantity * p.ProductPrice),2) AS Revenue,
        s.OrderNumber
    FROM sales s
    JOIN productlookup p
        ON s.ProductKey = p.ProductKey
    JOIN prodsubcat psc
        ON p.ProductSubcategoryKey = psc.ProductSubcategoryKey
    JOIN prodcat pc
      
        ON psc.ProductCategoryKey = pc.ProductCategoryKey
)
SELECT DISTINCT
    CategoryName,
    SUM(Revenue) OVER (PARTITION BY CategoryName) AS TotalRevenue,
    COUNT(OrderNumber) OVER (PARTITION BY CategoryName) AS TotalOrders
FROM category_sales
ORDER BY TotalRevenue DESC;

